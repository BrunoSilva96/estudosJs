const numA = Number(prompt("Digite um número:"));

const numB = Number(prompt("Digite mais um número:"));

const soma = numA + numB;
const subtrair = numA - numB;
const multiplicar = numA * numB;
const dividir = numA / numB;

alert(
`A soma entre os números é: ${soma},
A subtração entre os números é: ${subtrair},
A multiplicação entre os números é: ${multiplicar},
A divisão entre os números é: ${dividir}`
);
